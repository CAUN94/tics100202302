# Tarea: Juego de Cuadrado Rebote en Pygame

# Objetivo: Crear un juego en Pygame donde un cuadrado se mueva en una dirección aleatoria dentro de una ventana y rebote en los bordes. Además, se debe mostrar en la parte superior derecha de la ventana la cantidad de veces que el cuadrado ha rebotado.

# Instrucciones:

#     Utiliza la biblioteca Pygame para crear una ventana de juego con un ancho de 800 píxeles y una altura de 600 píxeles. Asegúrate de configurar un título apropiado para la ventana.

#     Dibuja un cuadrado azul en el centro de la ventana. El cuadrado debe tener un tamaño inicial de 50x50 píxeles.

#     Asigna al cuadrado una velocidad inicial en píxeles por fotograma utilizando random.randint(). La velocidad en ambas direcciones (horizontal y vertical) debe ser un número aleatorio entre 2 y 4 píxeles por fotograma. La dirección de la velocidad debe ser aleatoria, por lo que puede ser positiva o negativa en ambas direcciones.

#     Implementa el movimiento del cuadrado en la ventana. El cuadrado debe moverse en la dirección asignada y rebotar en los bordes de la ventana.

#     Muestra en la esquina superior derecha de la ventana la cantidad de veces que el cuadrado ha rebotado.

#     Continúa el juego hasta que se cierre la ventana.